
package basicjava;

public class IncreamentDecreament {
    public static void main(String[] args) {
        int x = 15;
    int y;
    
    y = --x; //14
    System.out.println(y);
    y = x--; // 14
    System.out.println(y);
    y = x++; //13
    System.out.println(y);
    y = ++x; //15
    System.out.println(y);
    
    
    }
}
