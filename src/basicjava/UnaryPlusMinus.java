
package basicjava;


public class UnaryPlusMinus {
    public static void main(String[] args) {
        int x = -10;
        
        int result;
        result = +x;
        
        System.out.println(result);
        
        result = -x;
        System.out.println(result);
        // Positive
        int y = 10;
        
        int result1;
        result1 = +y;
        
        System.out.println(result1);
        
        result1 = -y;
        System.out.println(result1);
    }
}
